
public class ThreeLine {

	public static void main(String[] args) {
		System.out.println("First line");
		printLines();
		System.out.println("Second line");
		printLines();
		System.out.println("Third line");
		printLines();
	}
	public static void printLines() {
		System.out.println();
		System.out.println();
		System.out.println();
	}
}
