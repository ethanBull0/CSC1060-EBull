public class SumOfNaturalNums {
	public static void main(String[] args) {
		
	}
public static int SumOfForNumbers() {
	int sumNums = 0;
	for (int i = 1; i < 10; i++) {
	sumNums += i;
	}
	return sumNums;
}
public static int SumOfWhileNumbers() {
	int sumNums = 0;
	int i = 1;
	while (i < 10) {
	sumNums += i;
	i++;
	}
	return sumNums;
}
}
